using UnityEngine;
using System.Collections;

public class Quaternion2 : MonoBehaviour {

    public float x;
    public float y;
    public float z;
    public float w; 

    public Quaternion2(float xpr, float ypr, float zpr, float wpr)
    {
        x = xpr;
        y = ypr;
        z = zpr;
        w = wpr;
    }
}
